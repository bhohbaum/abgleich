def humanize_size(size):

	suffix = 'B'

	for unit in ('', 'Ki', 'Mi', 'Gi', 'Ti', 'Pi', 'Ei', 'Zi', 'Yi'):
		if abs(size) < 1024.0:
			return "%3.1f %s%s" % (size, unit, suffix)
		size /= 1024.0

	raise ValueError('"size" too large')
